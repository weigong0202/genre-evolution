// Helper to find an artist by ID across all genres
export function findArtistById(artistId: string): { artist: Artist; genreId: string } | null {
  for (const [genreId, artists] of Object.entries(genreArtists)) {
    const artist = artists.find(a => a.id === artistId)
    if (artist) {
      return { artist, genreId }
    }
  }
  return null
}

// Helper to find artists by name (for influence lookups)
export function findArtistsByName(name: string): Artist[] {
  const results: Artist[] = []
  for (const artists of Object.values(genreArtists)) {
    const match = artists.find(a => a.name.toLowerCase() === name.toLowerCase())
    if (match) {
      results.push(match)
    }
  }
  return results
}
